package packageBezier;

public class Point {
    int x;
    int y;
    
    Point(){
        
    }
    
    Point(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getValorX(){
        return this.x;
    }
    
    public int getValorY(){
        return this.y;
    } 
}
